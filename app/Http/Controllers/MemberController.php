    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $member = Member::latest()->first() ?? new Member();
        $kode_member = (int) $member->kode_member +1;

        $member = new Member();
        $member->kode_member = tambah_nol_didepan($kode_member, 5);
        $member->nama = $request->nama;
        $member->telepon = $request->telepon;
        $member->alamat = $request->alamat;
        $member->save();

        return response()->json('Data berhasil disimpan', 200);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $member = Member::find($id);

        return response()->json($member);
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param int $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        //
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param int $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $member = Member::find($id)->update($request->all());

        return response()->json('Data berhasil disimpan', 200);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $member = Member::find($id);
        $member->delete();

        return response(null, 204);
    }

    public function cetakMember(Request $request)
    {
        // Mengambil data member berdasarkan ID yang diterima
        $datamember = Member::findMany($request->id_member);

        // Membagi data member menjadi chunk berisi 2 item per chunk
        $datamember = $datamember->chunk(2);

        // Mengambil pengaturan pertama
        $setting = Setting::first();

        // Nomor awal untuk loop di view
        $no = 1;

        // Membuat PDF dengan menggunakan view 'member.cetak'
        $pdf = PDF::loadView('member.cetak', compact('datamember', 'no', 'setting'));

        // Mengatur ukuran kertas dan orientasi
        $pdf->setPaper([0, 0, 566.93, 850.39], 'portrait');

        // Mengembalikan file PDF sebagai stream
        return $pdf->stream('member.pdf');
    }

    public function showMemberByCode($kode_member)
    {
        // Cari member berdasarkan kode_member
        $member = Member::where('kode_member', $kode_member)->first();

        // Jika member ditemukan, tampilkan view detail member
        if ($member) {
            return view('member.show', compact('member'));
        } else {
            // Jika member tidak ditemukan, tampilkan pesan error
            return abort(404, 'Member not found.');
        }
    }


